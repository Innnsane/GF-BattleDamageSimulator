void influence_calculate()
{

    influence_select();
    for (int pos = 1 ; pos < 10 ; pos++ ) {
       if(doll[pos].id == 0)continue;
       doll[pos].attack *= (1+doll[pos].attack_inf);
       doll[pos].fire_rate *= (1+doll[pos].fire_rate_inf);
       doll[pos].hit *= (1+doll[pos].hit_inf);
       doll[pos].dodge *= (1+doll[pos].dodge_inf);
       doll[pos].critical_per *= (1+doll[pos].critical_per_inf);
       doll[pos].armor *= (1+doll[pos].armor_inf);
       doll[pos].skill1_cd = ceil( doll[pos].skill1_cd * max( 0 , 1 -  min( 0.3 , doll[pos].cd_inf )));
       doll[pos].skill1_before = ceil( doll[pos].skill1_before * max( 0 , 1 -  min( 0.3 , doll[pos].cd_inf )));
    }
}












